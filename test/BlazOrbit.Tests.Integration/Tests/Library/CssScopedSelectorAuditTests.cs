using FluentAssertions;
using System.Text.RegularExpressions;

namespace BlazOrbit.Tests.Integration.Tests.Library;

/// <summary>
/// CSS-SCOPED-09 (mecanizado): detecta selectores muertos en archivos .razor.css —
/// clases CSS declaradas en el CSS scoped cuyo nombre no aparece en el .razor
/// del mismo componente.
///
/// Un selector muerto es un bug silencio: el CSS se emite en el bundle pero
/// nunca coincide con el DOM real del componente.
/// </summary>
[Trait("Library", "CssAudit")]
public class CssScopedSelectorAuditTests
{
    private static readonly string SrcBlazOrbit = Path.GetFullPath(Path.Combine(
        Directory.GetCurrentDirectory(),
        "..", "..", "..", "..", "..",
        "src", "BlazOrbit"));

    // Classes that may appear in .razor.css but are injected dynamically by JS
    // or generated by the framework (not present as literals in the .razor file).
    private static readonly HashSet<string> DynamicClassAllowlist = new(StringComparer.Ordinal)
    {
        // JS-driven transition classes
        "transition-target",
        // Classes added by Blazor event handling infrastructure
        "blazor-error-boundary",
    };

    /// <summary>
    /// For each .razor.css, every class name used in a selector must appear
    /// literally in the corresponding .razor file (or in the allowlist).
    /// </summary>
    [Fact]
    public void RazorCss_Should_Not_Contain_Dead_Class_Selectors()
    {
        string[] cssFiles = Directory.GetFiles(SrcBlazOrbit, "*.razor.css", SearchOption.AllDirectories)
            .Where(f => !f.Contains($"{Path.DirectorySeparatorChar}obj{Path.DirectorySeparatorChar}"))
            .ToArray();

        cssFiles.Should().NotBeEmpty("there must be scoped CSS files to audit");

        List<string> violations = [];

        foreach (string cssFile in cssFiles)
        {
            string razorFile = Path.ChangeExtension(cssFile, ".razor");
            if (!File.Exists(razorFile))
            {
                continue; // orphaned CSS without Razor — already caught by other audits
            }

            string cssContent = File.ReadAllText(cssFile);
            string razorContent = File.ReadAllText(razorFile);
            string componentName = Path.GetFileNameWithoutExtension(cssFile);

            // Extract all class names used in CSS selectors
            HashSet<string> cssClasses = ExtractClassNamesFromCss(cssContent);

            // Extract all class names and data-attribute names used in the Razor markup
            HashSet<string> razorClasses = ExtractClassNamesFromRazor(razorContent);
            HashSet<string> razorDataAttrs = ExtractDataAttributesFromRazor(razorContent);

            foreach (string cls in cssClasses)
            {
                if (DynamicClassAllowlist.Contains(cls))
                {
                    continue;
                }

                // If the class is used in the Razor markup, it's alive
                if (razorClasses.Contains(cls))
                {
                    continue;
                }

                // Some selectors target family-shared classes that are emitted
                // by the base component (e.g. bob-input__field). We only flag
                // component-specific classes (bob-<component>__*).
                string prefix = $"bob-{KebabFromPascal(componentName.TrimStart('_'))}";
                if (!cls.StartsWith(prefix, StringComparison.Ordinal))
                {
                    // Family-shared class — may be emitted by base or child component;
                    // skip to avoid false positives.
                    continue;
                }

                violations.Add($"{componentName}: .{cls} has no literal usage in {Path.GetFileName(razorFile)}");
            }
        }

        violations.Should().BeEmpty(
            because: "every class selector in a .razor.css must reference a class that is " +
                     "literally applied in the corresponding .razor file. Dead selectors bloat " +
                     "the bundle and hide refactoring drift. See CSS-SCOPED-09.\n\n" +
                     string.Join("\n", violations));
    }

    private static HashSet<string> ExtractClassNamesFromCss(string content)
    {
        HashSet<string> result = new(StringComparer.Ordinal);

        // Remove CSS comments
        string clean = Regex.Replace(content, @"/\*.*?\*/", string.Empty, RegexOptions.Singleline);

        // Find class selectors: .classname
        // We intentionally do NOT require a word boundary after the class name
        // because pseudo-classes (::after, :hover) may follow immediately.
        foreach (Match m in Regex.Matches(clean, @"\.([a-zA-Z_-][a-zA-Z0-9_-]*)"))
        {
            result.Add(m.Groups[1].Value);
        }

        return result;
    }

    private static HashSet<string> ExtractClassNamesFromRazor(string content)
    {
        HashSet<string> result = new(StringComparer.Ordinal);

        // class="..." or class='...'
        foreach (Match m in Regex.Matches(content, @"class\s*=\s*""([^""]*)"""))
        {
            foreach (string part in m.Groups[1].Value.Split([' ', '\t'], StringSplitOptions.RemoveEmptyEntries))
            {
                if (!part.StartsWith("@") && Regex.IsMatch(part, @"^[a-zA-Z_-][a-zA-Z0-9_-]*$"))
                {
                    result.Add(part);
                }
            }
        }

        foreach (Match m in Regex.Matches(content, @"class\s*=\s*'([^']*)'"))
        {
            foreach (string part in m.Groups[1].Value.Split([' ', '\t'], StringSplitOptions.RemoveEmptyEntries))
            {
                if (!part.StartsWith("@") && Regex.IsMatch(part, @"^[a-zA-Z_-][a-zA-Z0-9_-]*$"))
                {
                    result.Add(part);
                }
            }
        }

        // C# string literals that look like CSS classes (e.g. inside .razor.cs inline)
        foreach (Match m in Regex.Matches(content, "\"(bob-[a-zA-Z0-9_-]+(?:__[a-zA-Z0-9_-]+)?(?:--[a-zA-Z0-9_-]+)?)\""))
        {
            result.Add(m.Groups[1].Value);
        }

        return result;
    }

    private static HashSet<string> ExtractDataAttributesFromRazor(string content)
    {
        HashSet<string> result = new(StringComparer.Ordinal);

        foreach (Match m in Regex.Matches(content, @"data-bob-[a-z0-9-]+\s*=\s*""[^""]*"""))
        {
            string attr = m.Value.Split(['=', ' '], StringSplitOptions.RemoveEmptyEntries)[0];
            result.Add(attr);
        }

        return result;
    }

    private static string KebabFromPascal(string pascal)
    {
        // BOBButton → button, BOBInputText → input-text
        if (pascal.StartsWith("BOB"))
        {
            pascal = pascal[3..];
        }

        return string.Concat(pascal.SelectMany((c, i) =>
            i > 0 && char.IsUpper(c)
                ? new[] { '-', char.ToLowerInvariant(c) }
                : new[] { char.ToLowerInvariant(c) }));
    }
}
