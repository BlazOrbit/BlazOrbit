namespace BlazOrbit.Components;

/// <summary>
/// Defines the position of a badge relative to its anchor element.
/// </summary>
public enum BadgePosition
{
    /// <summary>
    /// Positions the badge at the top-right corner.
    /// </summary>
    TopRight,

    /// <summary>
    /// Positions the badge at the top-left corner.
    /// </summary>
    TopLeft,

    /// <summary>
    /// Positions the badge at the bottom-right corner.
    /// </summary>
    BottomRight,

    /// <summary>
    /// Positions the badge at the bottom-left corner.
    /// </summary>
    BottomLeft
}
