// ComponentInfoGenerator.cs
// Proyecto: BlazOrbit.Generator  (netstandard2.0)
//
// PIPELINE OVERVIEW
// ─────────────────────────────────────────────────────────────────────────────
//  1. Lee todos los .razor como AdditionalFiles (texto plano).
//  2. Para cada .razor extrae: @namespace · @inherits · @attribute y el @code.
//  3. Parsea el @code con CSharpSyntaxTree (es C# puro) para obtener
//     [Parameter] + XML docs + default values.
//  4. Sólo los que tienen @attribute [GenerateComponentInfo] generan salida.
//  5. Resolución de herencia:
//       padre .razor → busca en el mapa de todos los .razor parseados
//       padre .cs    → busca en la Compilation de Roslyn (GetSymbolsWithName)
//       .cs → .cs   → sube BaseType en Roslyn sin límite hasta object
// ─────────────────────────────────────────────────────────────────────────────

