internal static class Diagnostics
{
    public static readonly DiagnosticDescriptor ComponentNameCollision = new(
        id: "BOBGEN001",
        title: "Multiple .razor components share the same simple name",
        messageFormat: "Multiple .razor files named '{0}' exist in different folders/namespaces ({1}); @inherits resolution prefers the same-namespace match and otherwise picks deterministically by file path. Rename one of them or fully-qualify @inherits to silence this warning.",
        category: "BlazOrbit.CodeGeneration",
        defaultSeverity: DiagnosticSeverity.Warning,
        isEnabledByDefault: true);

    public static readonly DiagnosticDescriptor AmbiguousBaseType = new(
        id: "BOBGEN002",
        title: "Ambiguous @inherits base type",
        messageFormat: "@inherits '{0}' matches multiple types in the compilation ({1}); resolution picked '{2}'. Fully-qualify @inherits with the namespace to silence this warning.",
        category: "BlazOrbit.CodeGeneration",
        defaultSeverity: DiagnosticSeverity.Warning,
        isEnabledByDefault: true);
}

// ─────────────────────────────────────────────────────────────
//  RazorIndex — multi-map of .razor files keyed by simple name
//  with collision detection. Built once per generation pass.
// ─────────────────────────────────────────────────────────────

internal sealed class RazorIndex
{
    private readonly Dictionary<string, List<RazorFileData>> _bySimpleName;

    public RazorIndex(IEnumerable<RazorFileData> files)
    {
        _bySimpleName = files
            .GroupBy(f => f.ComponentName, StringComparer.Ordinal)
            .ToDictionary(
                g => g.Key,
                g => g.OrderBy(f => f.FilePath, StringComparer.Ordinal).ToList(),
                StringComparer.Ordinal);
    }

    /// <summary>All entries with this simple name, deterministically sorted by file path.</summary>
    public IReadOnlyList<RazorFileData> Lookup(string simpleName)
        => _bySimpleName.TryGetValue(simpleName, out List<RazorFileData>? list)
            ? list
            : Array.Empty<RazorFileData>();

    /// <summary>
    /// Picks the best match for an @inherits resolved from <paramref name="consumerNamespace"/>.
    /// Same-namespace match wins; otherwise the first deterministic candidate.
    /// </summary>
    public RazorFileData? PickBest(string simpleName, string? consumerNamespace)
    {
        IReadOnlyList<RazorFileData> candidates = Lookup(simpleName);
        if (candidates.Count == 0)
        {
            return null;
        }

        if (candidates.Count == 1)
        {
            return candidates[0];
        }

        foreach (RazorFileData c in candidates)
        {
            if (StringComparer.Ordinal.Equals(c.DeclaredNamespace, consumerNamespace))
            {
                return c;
            }
        }

        return candidates[0];
    }

    /// <summary>Yields entries (simpleName, candidates) where there is more than one candidate.</summary>
    public IEnumerable<KeyValuePair<string, IReadOnlyList<RazorFileData>>> Collisions
    {
        get
        {
            foreach (KeyValuePair<string, List<RazorFileData>> kv in _bySimpleName)
            {
                if (kv.Value.Count > 1)
                {
                    yield return new KeyValuePair<string, IReadOnlyList<RazorFileData>>(kv.Key, kv.Value);
                }
            }
        }
    }
}

