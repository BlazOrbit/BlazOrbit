/// <summary>
/// Describes a single Blazor component parameter discovered at build time.
/// </summary>
/// <param name="Name">Parameter name.</param>
/// <param name="Type">CLR type name of the parameter.</param>
/// <param name="Default">Literal default value, if any.</param>
/// <param name="Description">XML doc summary, if available.</param>
public sealed record ParameterData(
    string Name,
    string Type,
    string? Default,
    string? Description);

/// <summary>
/// Aggregated metadata for a Blazor component extracted from its <c>.razor</c> file.
/// </summary>
/// <param name="Namespace">Declared namespace.</param>
/// <param name="ComponentName">Type name (derived from the file name).</param>
/// <param name="Parameters">Parameters declared in the <c>@code</c> block.</param>
public sealed record ComponentData(
    string Namespace,
    string ComponentName,
    ImmutableArray<ParameterData> Parameters);

/// <summary>Toda la información extraída de un único archivo .razor.</summary>
public sealed record RazorFileData(
    string FilePath,
    string ComponentName,        // del nombre de archivo
    string? DeclaredNamespace,    // de @namespace
    string? InheritsRaw,          // de @inherits (texto en crudo)
    ImmutableArray<ParameterData> OwnParameters,        // [Parameter] propios del @code
    bool GenerateInfo);        // true si @attribute [GenerateComponentInfo]

// ─────────────────────────────────────────────────────────────
//  Shared sources — se inyectan en la compilación de BlazOrbit
// ─────────────────────────────────────────────────────────────

internal static class SharedSources
{
    public const string GenerateComponentInfoAttribute =
        """
        // <auto-generated/>
        namespace BlazOrbit;

        /// <summary>
        /// Marks a Blazor component for automatic documentation generation.
        /// Add <c>@attribute [GenerateComponentInfo]</c> to the .razor file,
        /// or <c>[GenerateComponentInfo]</c> to a .cs component class.
        /// </summary>
        [System.AttributeUsage(System.AttributeTargets.Class,
            AllowMultiple = false, Inherited = false)]
        public sealed class GenerateComponentInfoAttribute : System.Attribute { }
        """;

    // Mismo namespace que el atributo → un solo "using BlazOrbit;" en los generados.
    public const string ComponentParameterInfo =
        """
        // <auto-generated/>
        #nullable enable

        namespace BlazOrbit;

        /// <summary>Describes a single Blazor component parameter.</summary>
        public sealed record ComponentParameterInfo(
            string  ParameterName,
            string  ParameterType,
            string? ParameterDefault,
            string? ParameterDescription);
        """;
}

